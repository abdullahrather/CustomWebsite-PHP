                    <h2>About Bowel Cancer</h2>

                    <p>Bowel cancer is very common, and in fact <a href="https://www.bowelcanceruk.org.uk/media/">someone is diagnosed with it every 15 minutes</a>. It is one of the most treatable types of cancer, but for the best outcome it must be caught early. Sadly, it often isn&rsquo;t caught until it is quite advanced, and this is because many people do not recognise the symptoms, putting changes in bowel habits down to environmental factors like dietary changes or stress. People also put off going to their GP or discussing their bowel habits with each other like they would other aspects of their health.</p>

                    <p><img alt="" height="425" src="images/media/190415101210.jpg" width="300" /></p>

                    <h2>Advances to screening procedures should increase early detection rates</h2>

                    <p>A new bowel cancer screening test, the <a href="https://www.faecal-immunochemical-test.co.uk/about-faecal-immunochemical-testing/">faecal immunochemical test (FIT)</a>, was rolled out late last year and it could prove more than twice as effective in <a>detecting </a>cancer than the previous system. It is much easier to complete the test, and requires people to collect only one poo sample, compared to three separate samples with the previous test. It is expected to increase testing uptake, because the less messy and awkward the testing procedure, the more likely people will be to take up the test.</p>

                    <h2>Know the risks and signs of bowel cancer.</h2>

                    <p>Routine screening can only go so far to detecting bowel cancer and it is important that people understand the risk factors and symptoms so they can lower their chance of developing it and get treatment as early as possible if they do.</p>

                    <p><a href="https://www.cancerresearchuk.org/about-cancer/bowel-cancer/risks-causes"><strong>Risk factors</strong></a><strong> that can increase the likelihood of developing bowel cancer:</strong></p>

                    <ul>
                        <li>Being overweight</li>
                        <li>Lack of <a href="exercise-can-you-get-too-much-of-a-good-thing.php">exercise</a></li>
                        <li>Not eating enough fibre</li>
                        <li>Eating too much red or processed meat</li>
                        <li>Drinking too much alcohol</li>
                        <li>Smoking tobacco</li>
                        <li>Family history</li>
                        <li>Medical conditions like Crohn&rsquo;s disease or ulcerative colitis, diabetes, gallstones and acromegaly</li>
                        <li>Advancing age - 40% of bowel cancer cases are in over 75s.</li>
                        <li>Infection of helicobacter plyori, a bacterial infection of the gut that can also cause stomach ulcers</li>
                    </ul>

                    <p><a href="https://www.bowelcanceruk.org.uk/support-us/bowel-cancer-awareness-month/for-healthcare-professionals/"><strong>Signs and symptoms</strong></a><strong> of bowel cancer:</strong></p>

                    <ul>
                        <li>A change in bowel habits that lasts longer than four weeks</li>
                        <li>Blood in the toilet bowl, on the toilet roll, or visible in your poo</li>
                        <li>Persistent flatulence, bloating, cramping or abdominal discomfort</li>
                        <li>Weakness or fatigue</li>
                        <li>Unintentional weight loss</li>
                        <li>A lump or swelling in your tummy</li>
                    </ul>

                    <p>If you notice any of the above symptoms, it is important to see your doctor to talk them over. People often self-diagnose conditions like IBS or <a>haemorrhoid</a>&nbsp; when in reality these conditions should only be diagnosed by a doctor to rule out more serious potential problems. Bowel cancer symptoms are often silent or vague in the early stages, but if we are more aware and open about these things, we stand a better chance of catching the disease early when it is treatable. Talking about poo could save your life.</p>

