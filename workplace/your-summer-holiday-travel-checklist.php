            <p>If you are one of the 56% of Brits jetting off over the summer for some R&amp;R, it may be unpleasant to think of what can go wrong on your hard-earned holiday - but it certainly pays to be prepared.</p>

<p>There are lots of extra hazards and health risks that many of us overlook when travelling abroad: We run you through our top seven tips for looking after your health these summer holidays.</p>

<ol>
	<li><strong>Take out travel insurance.</strong> It seems like a no-brainer, but according to the Association of British Travel Agents (ABTA) 8.6 million Brits do not have travel insurance for their summer holiday, with 37% responding to a survey stating they didn&rsquo;t think they needed it. Travel insurance does not only cover your finances and belongings, but it also covers you and your medical expenses should you or your party become unwell or injured whilst away. The costs for medical treatment or repatriation can run into the millions, and leave you stranded or without access to adequate healthcare. The importance of good travel insurance for all the family cannot be stressed enough.</li>
	<li value="2"><strong>Ensure you have an adequate supply of medications, plus extra to cover delays or loss. </strong>Delays, loss of baggage or spoilage could leave you without medication, and without access to your usual GP or pharmacy, it may not be simple to replenish your supply. Pack extras to be certain you have enough and make sure you bring your prescriptions too, especially if you are carrying medication through security in your hand luggage.<br />
	&nbsp;</li>
	<li value="3"><strong>Visit your GP for a check-up for pre-existing conditions. </strong>Your GP will be able to help you with any special considerations you may need for your pre-existing conditions, and provide you with a prescription note to pass through security with any restricted medications, as suggested above.<br />
	&nbsp;</li>
	<li value="4"><strong>Get any necessary vaccinations or medications like antimalarials. </strong>Contrary to popular opinion, medicinal Gin and Tonics sadly don&rsquo;t count as malaria prevention. The NHS recommends a range of vaccinations for different destinations - <a href="https://www.nhs.uk/conditions/travel-vaccinations/jabs/">check out</a> whether the area you are visiting is affected by diseases like yellow fever, cholera, typhoid and TB.<br />
	&nbsp;</li>
	<li value="5"><strong>Pack a first aid kit. </strong>A basic first aid kit can make all the difference to treating minor ailments and prevent them from turning into major ones. A small kit including antiseptic wipes and creams, antihistamines, painkillers, insect repellent and bite and sting relief, hydrocortisone cream, plasters, dressings and tape, tweezers and sunburn treatment is all you need.<br />
	&nbsp;</li>
	<li value="6"><strong>Be sun safe. </strong>Another no brainer. Getting sunburned only once every two years is all it takes to triple your <a href="https://www.cancerresearchuk.org/about-cancer/causes-of-cancer/sun-uv-and-cancer/how-does-the-sun-and-uv-cause-cancer">risk of getting skin cancer</a>. Much of the damage is done in childhood, with studies showing that a child must only <a href="https://www.nhs.uk/news/cancer/just-five-sunburns-increase-your-cancer-risk/">receive 5 sunburns to increase their skin cancer risk by 80%.</a> The NHS recommends that you wear a good quality sunscreen of at least 30SPF, avoid the sun during 11am and 3pm, and wear sunglasses and cover up with clothing.<br />
	&nbsp;</li>
	<li value="7"><strong>Know the emergency numbers and procedures of the country you are visiting. </strong>If the worst happened and you found yourself in an emergency situation, you want to be confident you would have access to help. In most EU countries, the emergency services number is 112.</li>
</ol>

