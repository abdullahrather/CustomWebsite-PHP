                    <h2>We all know exercise is important for our health, but what happens if you overdo it?</h2>

                    <p>As doctors and providers of affordable medical insurance, we are obviously keen that our clients have the healthiest lifestyle possible. &lsquo;Eat less and move more!&rsquo; we are instructed by every health organisation in the world. We are told to achieve wellness and <a href="give-your-heart-some-love-this-valentine-s-day.php">heart health</a> we should be getting at least <a href="https://www.who.int/dietphysicalactivity/factsheet_adults/en/">150 minutes of moderate activity every week.</a> Easier said than done for many of us, but for others the issue is finding that upper limit.</p>

                    <p>If you have caught the fitness bug, you know how great exercise can make you feel - endorphins flowing, increased energy, feeling stronger and like you could take on the world. Logically you would think that the more exercise you do the better the impact on your health, but that may not be the case.</p>

                    <h2>What are the risks of overdoing it?</h2>

                    <p>Well-conditioned athletes train for multiple hours every day, but not everyone can claim to be in the same kind of physical shape as an athlete. Overtraining can result in injury and trigger some unpleasant responses in your body.</p>

                    <p>We explore the ways in which too much exercise can affect you:</p>

                    <h2>Illness, fatigue and hormone imbalances</h2>

                    <p>Hormone imbalances, thyroid dysfunction, compromised immune system, slowed metabolic rate and even adrenal fatigue are all linked with over-exercising.</p>

                    <p>Adrenal fatigue is not a proven medical condition, but more a way to describe a sense of crushing tiredness that is thought to occur when the <a href="https://patient.info/hormones/adrenal-fatigue">adrenal glands</a> fail to cope when your body is under prolonged stress. The adrenal glands are responsible for producing certain hormones, including cortisol, known as the &lsquo;stress hormone&rsquo;.</p>

                    <p>Excess cortisol production has been linked with suppressing the immune system, leaving you more prone to the effects of viral infections. There is also some evidence that increased cortisol slows the metabolism, leading to weight gain - usually the opposite of the desired effect of exercise.When you push your body to its limits it can result in feeling drained and like sleep just cannot refresh you. Exercise should leave you feeling great - if it has the opposite effect, consider slowing down for a while.</p>

                    <h2>Risk of injury and wear &amp; tear</h2>

                    <p>There are two classifications of <a href="https://www.webmd.com/fitness-exercise/guide/workout-injuries-prevention-and-treatment">injury</a> - traumatic and cumulative. Traumatic injury occurs from one single event, like a fall, turned ankle or dropping a weight or using improper form. Cumulative injuries result from repetitive strain on joints, connective tissues or muscle fibres.</p>

                    <p>If your body is tired, your muscles cannot work optimally, and this can leave you at greater risk of a traumatic injury. To maintain proper form and control, your body must not be exhausted.</p>

                    <p>Cumulative injuries can result from poor posture or poor form, or even the wrong equipment. Most often it can be put down to high impact activities over prolonged periods.</p>

                    <p>High impact exercises like running or plyometrics can be great for strengthening bones and muscles but can be traumatic for joints and ligaments.</p>

                    <h2>How to know when enough is enough</h2>

                    <p>Signs of overtraining include fatigue, pain and decreased performance.</p>

                    <p>Aching muscles a day or two after exercise, known as <a href="https://www.nhs.uk/live-well/exercise/pain-after-exercise/">Delayed Onset Muscle Soreness (DOMS)</a> are normal, but pain that begins during or immediately after a workout is not normal and is a signal to stop, rest, and treat the painful area.</p>

                    <p>It is important to listen to your body, but take regular full rest days even if you feel fine. Engage in active recovery like stretching, walking and leisurely swimming. Also, if you do strength training, consider cross-training. This means planning your workouts to focus on different areas, so as not to stress particular body parts and give muscles time to recover. It is during recovery that muscles repair themselves and grow stronger.</p>

                    <p>A commitment to regular exercise is one of the greatest gifts you can give your body, but as with most things in life, balance is key to reaping the benefits.</p>

