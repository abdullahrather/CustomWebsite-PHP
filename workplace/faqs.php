<h3><span style="display: none;">&nbsp;</span><span style="display: none;">&nbsp;</span>Is there an excess?</h3>

<p><span style="color:#000000;">Yes, you must pay the first &pound;100 of the first claim in each policy year.</span></p>

<h3>What underwriting options are available?</h3>

<h4><strong>Moratorium</strong></h4>

<p><span style="color:#000000;">Unless otherwise specified in the policy certificate, <strong>you will not be covered for pre-existing conditions</strong> (or related medical conditions) that you have suffered from in the <strong>five-year period</strong> prior to your commencement date.</span></p>

<p><span style="color:#000000;">You will be covered for claims related to these pre-existing conditions and related medical conditions once you have been free of symptoms, treatment and advice for two continuous years from your commencement date.</span></p>

<h4><strong>Medical Underwriting</strong></h4>

<p><span style="color:#000000;">Your policy schedule will show exclusions for any conditions that are deemed to relate to pre-existing conditions based on the information that you have provided. Your policy schedule will also show whether we can remove the exclusion after a period of time.</span></p>

<h3>Why are there limitations on chronic and pre-existing conditions?</h3>

<p><span style="color:#000000;">To make our premiums competitive we have assumed our membership will be representative of the population as a whole.&nbsp; If we were to accept pre-existing conditions at the time of joining, our membership profile would become skewed in favour of high-risk individuals, raising the incidence of claims and, subsequently, our premiums. We do, however, accept pre-existing conditions after two years provided no medical input has been required in this time.</span></p>

<p><span style="color:#000000;">Similarly, we limit benefits for chronic conditions as the cost of their long-term treatment can be very significant; this would also impact on your premium. This approach is adopted by most medical insurance companies.</span></p>

<h3>What are pre-existing conditions?</h3>

<p><span style="color:#000000;">A pre-existing condition is any condition for which a member has already received medical advice or treatment prior to their enrolment into the scheme or for which symptoms were present and a prudent person would have sought treatment. This definition also includes complications that may arise as a direct consequence of such a condition.</span></p>

<h3>What is a chronic illness or condition?</h3>

<p><span style="color:#000000;">&nbsp;A&nbsp;chronic medical condition is defined as such if it meets any of the following criteria:</span></p>

<ul>
	<li><span style="color:#000000;">It needs ongoing or long-term monitoring through consultations, examinations, check-ups and/or tests</span></li>
	<li><span style="color:#000000;">It needs ongoing or long-term control for relief of symptoms</span></li>
	<li><span style="color:#000000;">It requires your rehabilitation or for you to be specially trained to cope with it</span></li>
	<li><span style="color:#000000;">It continues indefinitely</span></li>
	<li><span style="color:#000000;">It has no known cure</span></li>
	<li><span style="color:#000000;">It comes back or is likely to come back</span></li>
	<li>Examples of some common conditions that fall into this category include: diabetes, asthma, ulcerative colitis, Crohn&#39;s disease, coeliac disease, rheumatoid arthritis, Barrett&rsquo;s esophagitis, endometriosis, stroke, osteoarthritis, multiple sclerosis, ischemic heart disease, chronic airway disease, cardiac failure, connective tissue disorder</li>
</ul>

