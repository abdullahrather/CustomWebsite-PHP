<h3><span style="color: rgb(1, 155, 214); font-weight: 600; text-transform: uppercase;">How</span><strong><span style="color: rgb(1, 155, 214); font-weight: 600; text-transform: uppercase;"> do I choose my specialist for my outpatient diagnosis and investigations?</span></strong></h3>

<ul>
	<li>When you contact us for authorisation for an outpatient<span style="font-size:14px;"> consultation</span> we will give you a choice of whether you want to use our network or you would prefer to go outside it.</li>
	<li>If you use our network we will give you a list of specialists from which to choose. These doctors will have admitting rights at our recognised hospitals.</li>
	<li>You may choose to go outside our network, in which case you can decide upon your own specialist based on advice from your GP or other recommendations. Your specialist will have to be on the specialist register with a licence to practice in the area of specialism you are consulting them about. We will give you a budget in line with what we pay our network doctors and will reimburse against any invoice you receive up to the agreed amount.</li>
	<li>Should you be unclear regarding your options, or who to choose, our specialist doctors can always help you.</li>
</ul>

<h3>How do I choose my specialist for a procedure?</h3>

<ul>
	<li>If you are seeking our authorisation for a procedure, you will already have seen a specialist. If you saw this specialist privately we would advise you to stay with them for continuity of care.</li>
	<li>If the specialist you saw was on our network then your procedure will be done in one of our recognised hospitals and they will bill us directly and we will settle the bill with them</li>
	<li>If the specialist you saw was out of our network, then we will give you a budget for your procedure in line with what we would pay our network partners and we would refund you up to this amount against an invoice. If the cost of the procedure is more then you will have to fund the difference. If the specialist can perform the surgery in one of our recognised&nbsp;hospitals,&nbsp;despite not being in our network (Specialist often&nbsp;have&nbsp;admitting&nbsp;privileges in multiple hospitals) we would be able to refund the hospital but not the doctor directly. <em>Please note the doctor who treats you has to be on the specialist register with a license to practice in the area of specialism you are consulting them about</em>.</li>
	<li>If you saw the specialist on the NHS and now want to claim on your policy for the procedure, we will again give you a choice of whether you want to use our network or go outside it. If you choose to go within our network, we will give you a choice of a specialist with practising privileges in our approved hospital. We will pay for your initial consultation with the specialist but we will not be able to pay for any further outpatient investigations unless you have cover for this. We will then pay the specialist and hospital directly.</li>
	<li>If you choose to go out of network, we will let you choose a specialist and set you a budget for the procedure in line with what we would pay our network partners. We would not be able to pay for any outpatient investigations unless you have cover for this. You may have to make up any shortfall. Many hospitals offer package deals for procedures.</li>
	<li>Should you be unclear regarding your options or who to choose. Our specialist doctors can always help you.</li>
</ul>

