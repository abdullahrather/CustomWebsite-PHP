                    <p>The chances are, you have been affected by heart disease in some way. Around 7 million people are living with cardiovascular disease (CVD) in the UK, and a quarter of all deaths are caused by heart conditions.</p>

                    <p>Many cases of heart disease are preventable simply by making choices with your heart health in mind. In honour of Saint Valentine, here are 14 ways you can love your heart this 14th February:<br />
                        <br />
                        1.&nbsp;&nbsp; &nbsp;Quit Smoking. Smoking kills about half of all persistent smokers and causes around 10% of all cases of CVD. We can&rsquo;t sugar-coat it: Smoking has no benefits and is terrible for your health. Contact your GP for support to quit as soon as you can.<br />
                        <br />
                        2.&nbsp;&nbsp; &nbsp;Stay active. We all know exercise has a major impact on our health, yet 40% of adults in the UK are not getting enough of it. Recommendations are that you should get at least 150 minutes of moderate physical activity per week.
                    </p>

                    <p><img alt="" src="images/media/190213020834.jpg" style="width: 452px; height: 301px;" /></p>

                    <p>Source: gov.uk<br />
                        <br />
                        3.&nbsp;&nbsp; &nbsp;Reduce the time you spend sitting. Even if you do build enough activity into your week, there is evidence that you can undo your hard work by sitting for too long the rest of the time.<br />
                        <br />
                        4.&nbsp;&nbsp; &nbsp;Manage your weight: Your weight, but more importantly the amount of fat you have around your abdomen, can affect your risk of developing heart disease. Even modest fat loss in obese people can have a significant impact on the risk of CVD. Don&rsquo;t get hung up on targets &ndash; if you are overweight, every pound lost is a step in the right direction.<br />
                        <br />
                        5.&nbsp;&nbsp; &nbsp;Eat less salt. Consuming too much salt can lead to high blood pressure, which can lead to heart disease and stroke. The BHF recommends a daily sodium intake of less than 6g<br />
                        <br />
                        6.&nbsp;&nbsp; &nbsp;Eat more oily fish. Oily fish, like salmon and mackerel, are a rich source of long-chain omega-3 fatty acids, which are proven to improve heart health. There are plant-based sources of omega-3s, like dark green leafy vegetables, walnuts and flaxseed.<br />
                        <br />
                        7.&nbsp;&nbsp; &nbsp;Avoid trans fats. Diet advice can be confusing. One minute, fats are bad, then they&rsquo;re good, now we&rsquo;re telling you they are bad again. Not all fats are bad, but the scientific community are unanimous that trans fats are unhealthy. You will find them in processed snack foods like crisps, fried foods and margarine.<br />
                        <br />
                        8.&nbsp;&nbsp; &nbsp;Drink less alcohol. The good news is that moderate drinking can have a mild protective effect on the heart owing to the relaxing effects of alcohol. The bad news is that drinking too much will guarantee you an increased risk of heart disease.<br />
                        <br />
                        9.&nbsp;&nbsp; &nbsp;Eat your 5 a day. We all know we need to eat 5 portions of fruit and veg a day, but why? Fruit and veg are usually low in calories, so can help to keep your weight down. They are also rich in vitamins and minerals, like potassium, which keeps your blood pressure low.
                    </p>

                    <p><img alt="" src="images/media/190213020918.jpg" style="width: 431px; height: 192px;" /></p>

                    <p>Source: The British Heart Foundation</p>

                    <p>10.&nbsp;&nbsp; &nbsp;Eat more wholegrains and fibre. Whole grains like oats and other fibre rich foods can help you improve your blood cholesterol, and that is good for your heart because high cholesterol can lead to heart disease. Fibre also keeps you fuller for longer, helping you to control your weight in a heart health double whammy.<br />
                        <br />
                        11.&nbsp;&nbsp; &nbsp;See your dentist. There is a surprising link between oral health and heart disease. Poor dental health can increase the risk of a bacteria entering your blood stream, which can damage the valves of your heart.<br />
                        <br />
                        12.&nbsp;&nbsp; &nbsp;Manage your blood pressure. See your doctor or pharmacist for blood pressure check, or invest in a home blood pressure monitor. If you see an upwards trend in your blood pressure, your doctor will be able to help you reduce it before it causes damage to your heart and arteries.<br />
                        <br />
                        13.&nbsp;&nbsp; &nbsp;Reduce stress. Chronic stress can affect the blood pressure, which can put a strain on your cardiovascular system, but it can also lead to poor health through negatively influencing lifestyle factors like alcohol intake, activity levels and comfort-eating.<br />
                        <br />
                        14.&nbsp;&nbsp; &nbsp;Get a good night&rsquo;s sleep. Insufficient sleep leads to higher-than-normal levels of stress hormones and inflammation markers, which contribute to CVD. Lack of sleep also interferes with hormones that regulate appetite, contributing to obesity. 6 hours per night minimum will help you keep your heart healthy.<br />
