<p><strong>What is a Groin Hernia</strong></p>

<p>Groin hernias are remarkably common. It is estimated that in their life time 27% of men and 3% of women will develop such a condition. A hernia is defined as a weakness in the muscle wall through which the abdominal contents can protrude.</p>

<p>In the groin the muscle arrangements are such that areas of weakness do exist and this can lead to the hernia developing. In male babies, additionally, the testes are formed inside the body and descend later to their definitive position, dragging their blood supply with them. This process creates additional areas of weakness and hence the greater frequency of hernias in men. Depending on the exact position groin hernias are defined as inguinal or the less common femoral hernia.<br />
&nbsp;</p>

<p><strong>Can we prevent a groin hernia?</strong></p>

<p>While there may not be much one can do to prevent a groin hernia occurring, anything that increases intra-abdominal pressure such as straining, coughing, constipation or lifting will increase the likelihood, and it is often after such an event people say they first noticed a hernia.</p>

<p>Many hernias when they first present do not cause any problems, while others may cause intermittent discomfort or pain, which can be significant. Remember there are other causes of groin pain, which need to be excluded before the pain is attributed to the hernia. Small early hernia can be diagnosed using modalities such as ultrasound or MRI (magnetic nuclear resonance).<br />
&nbsp;</p>

<p><strong>Do I need surgery for a hernia?</strong></p>

<p>While many small and unproblematic hernias can be managed without surgery the main risk is of strangulation of the bowel, this occurs when the bowel gets trapped in the hernia and its blood supply is compromised. Large hernia can be troublesome due to their sheer size and can impact on a person&rsquo;s life style. Trusses may help manage hernia but in reality, they can be more of a hindrance than support.</p>

<p>Surgical management of groin hernias involves a small incision in the groin over the hernia, the protruding sac is excised and the defect is closed with a mesh. Non-mesh repairs are also feasible. The main risks of surgery are recurrence of the hernia and discomfort. The incidence of these is small but needs to be considered before any surgery. &nbsp;Alternative to open surgery is keyhole surgery, but this is usually reserved for recurrent hernia repair or where there are bilateral groin hernias.</p>

<p>It is estimated that over 70,000 groin hernia operations take place in the UK per year and most of these are done as day cases.</p>

