                    <p class="normal"><span lang="EN">When you are looking to improve your health, the idea of foods with magical properties that can even undo the damage of unhealthy habits is appealing. But, are superfoods much like other magical entities - merely a fairytale.</span></p>

                    <p class="normal"><span lang="EN">There&rsquo;s no official &ldquo;superfood rubberstamping department&rdquo; anywhere, and not a great deal of peer-reviewed research into which foods deserve superfood status. So there is very little reliable evidence to be able to provide sound medical advice, but that is not to say that all foods are equal. There are certainly some foods which are nutritional powerhouses, and others that are merely tummy-fillers.<o:p></o:p></span></p>

                    <h2><span lang="EN">Is it really possible for a food to give us any benefits beyond its nutritional content?<o:p></o:p></span></h2>

                    <p class="normal"><span lang="EN">There are absolutely some foods that contain compounds which are able to positively affect our health. Examples of such beneficial compounds are antioxidants, anti-inflammatory carotenoids and omega-3 fatty acids. There is also a trend towards focusing on our gut health and encouraging healthy gut flora with fermented foods like kefir. But the idea that food alone has the power to stave off <a href="bowel-cancer-awareness-we-need-to-talk.php"><span style="color:#1155CC">cancer</span></a> or <a href="give-your-heart-some-love-this-valentine-s-day.php"><span style="color:#1155CC">heart disease</span></a> is, frankly, an out and out lie. It&rsquo;s marketing - and that marketing is way more impactful on our minds than the qualities of the foods are on our bodies.<o:p></o:p></span></p>

                    <p class="normal"><span lang="EN">As a result of marketing tactics and media coverage, often we feel we must incorporate new food fads into our diets when there is actually very little scientific basis for it. People are trying to figure out how they can incorporate foods they find unpalatable into their diets because they believe in their &ldquo;must-have&rdquo; nutritional status. If you just can&rsquo;t learn to enjoy avocado, there&rsquo;s no need to disguise it as &ldquo;vegan chocolate mousse&rdquo;, just eat something else you do enjoy.<o:p></o:p></span></p>

                    <h2><span lang="EN">Aim for a super plate, not specific superfoods.<o:p></o:p></span></h2>

                    <p class="normal"><span lang="EN">All unprocessed, natural foods should be considered &ldquo;super&rdquo; for you. Aim for a super day, or a super plate, rather than specific superfoods.<o:p></o:p></span></p>

                    <p class="normal"><span lang="EN">The danger of elevating the status of certain foods is that people might restrict their diet to over-include them, or may overeat in order to include them. There is also a danger that if people believe there are somehow magic effects of superfoods, they can counteract or &ldquo;buy&rdquo; unhealthy choices, rather than making long term changes to improve their health by excluding these damaging foods.<o:p></o:p></span></p>

                    <h2><span lang="EN">The key to health and vitality is a balanced diet.<o:p></o:p></span></h2>

                    <p class="normal"><span lang="EN">Eating a varied and balanced diet, high in fresh, unprocessed foods will naturally include many of the beneficial compounds, that superfoods make bold claims about, without thinking about it. Good fats, dark leafy greens, a rainbow of fruit and vegetables, healthy fats, lean unprocessed meat and lots of fibre are the recipe for dietary success. There is no arbitrary list of specific foods you must eat to be healthy.<o:p></o:p></span></p>

