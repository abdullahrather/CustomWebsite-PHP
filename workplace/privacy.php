<h3><span style="font-size:22px;"><strong>OUR PRIVACY POLICY</strong></span></h3>
<img src="images/privacy.png" />
<p>We are committed to protecting your personal data and complying with data protection laws. Please read this privacy policy carefully. It provides information about how we use personal data and the rights available to you under data protection laws.</p>

<h4><span style="color:#339999;"><strong>1. LAYERED APPROACH </strong></span></h4>

<p>We use a layered approach to delivering information about data protection through:</p>

<p>- A brief privacy notice in insurance policy documentation when we issue your policy, which will refer to this more detailed privacy policy;<br />
- This privacy policy accessible online;<br />
- <a href="file:///C:/Users/gcrumbley/Downloads/LMA-Insurance-Market-Information-Uses-Notice-post-enactment-31-05-2018.pdf" target="_blank">The London Market Core Uses Information Notice</a>. This Notice provides further information on how personal data is used within the insurance market.</p>

<h4><span style="color:#339999;"><strong>2. INFORMATION WE MAY COLLECT ABOUT YOU</strong></span></h4>

<p>We may collect and process the following personal data about you:</p>

<p>- information provided to us through enquiry, application or claims forms, including:<br />
- information such as your name, address or contact details;<br />
- sensitive information (e.g. medical information) obtained either through you or third parties;<br />
- we may maintain records of any correspondence with you including phone records;<br />
- information we collect through cookies. Please see our cookies statement for further information.</p>

<h4><span style="color:#339999;"><strong>3. WHY WE COLLECT PERSONAL DATA</strong></span></h4>

<p>We are required by data protection laws to have a legitimate reason to process and use your personal data. The main purpose for processing your personal data is for the provision and performance of an insurance contract e.g. reviewing your application, setting you up as a policyholder, administration and maintenance of your insurance policy or in order to process a valid claim.</p>

<p>With regards to &lsquo;Special Category&rsquo; data i.e. sensitive information, we must obtain your consent at the point of collecting your personal data (i.e. when you input your details online or when completing a form) in order to process the data unless an exemption for insurance purposes is applicable. In some circumstances, if we do not have your consent to process your data, it may impact our ability to issue you with insurance cover or to handle any claims.</p>

<p>We may also gather personal data for the following purposes:</p>

<p>- In order to prevent, identify and investigate fraud or any activity that is in the public interest;<br />
- To defend ourselves against or make any legal claims;<br />
- Where we have lawful purpose for processing your data e.g. for maintaining our accounts and records, gathering market intelligence in order to develop and improve our products and services.</p>

<p>We will ensure that the processing of your personal data does not affect your rights under applicable data protection laws; to comply with a legal or regulatory obligation e.g. where we are required to maintain records of any transactions with you, or for compliance with international legal and regulatory authorities.</p>

<h4><span style="color:#339999;"><strong>4. SHARING AND SAFEGUARDING YOUR PERSONAL DATA</strong></span></h4>

<p>All personal data supplied to us is stored on secure servers and only accessed and used in line with our data protection policies and procedures. Your personal data will only be accessed by our employees or authorised third parties who require the information for their business purposes. In circumstances where it is necessary to share your personal data with a third party, there are contractual agreements in place to ensure the security and confidentiality of your personal data and the information will only be used for the specific purpose for which it has been provided to them.</p>

<p><span style="color:#339999;"><strong>4.1. OUR GROUP COMPANIES</strong></span><br />
We may share your personal data with our group companies, based in the UK, but only for the purposes laid out in this privacy policy and we will ensure the security of your personal data.<br />
<br />
<span style="color:#339999;"><strong>4.2. AUTHORISED THIRD PARTIES</strong></span><br />
We may also be required to allow authorised third parties, including service providers and suppliers, access to your personal data, for the purposes stated in section 2 of this notice. Any data sharing with third parties will be in compliance with applicable data protection laws.<br />
<br />
<span style="color:#339999;"><strong>4.3. GOVERNMENTAL, LEGAL AND REGULATORY AUTHORITIES</strong></span><br />
It may be necessary for us to share your personal data with financial and regulatory organisations (e.g. the Financial Ombudsman Service, the Financial Conduct Authority, the Information Commissioner&rsquo;s Office) or law enforcement agencies (including courts) in order to assist them with enquiries, investigations or proceedings and ensure our compliance with our regulatory and legal requirements. As a financial services company, we are required to have certain processes in place with regards to anti-bribery and corruption, money laundering and fraud. If any criminal offence is detected or suspected, we may share data with third parties (e.g. law enforcement agencies, fraud prevention agencies, anti-money laundering agencies) in order to prevent crime or aid investigations if crime is identified. We may also access this data as part of our &lsquo;Know Your Client&rsquo; procedures to establish the parties we are dealing with and when assessing a claim payment in order to prevent criminal offences.</p>

