            <p>18th October is Wear it Pink day, the flagship campaign of national breast cancer charity, Breast Cancer Now.</p>

<p><a href="https://breastcancernow.org/about-us/becoming-breast-cancer-now">Breast Cancer Now</a> believes that by 2050 everyone who develops breast cancer will live - and live well. These are bold aims that we are happy to get behind at County Medical. Though we do not offer cancer care under our health insurance, we can provide our Cancer Benefit option as an add-on to your policy that would provide a pay-out should you develop cancer. We want to help provide peace of mind at a difficult time, but equally, we would like fewer people to need to access that safety net.</p>

<h2>The whole of October is dedicated to Breast Cancer Awareness Month.</h2>

<p>There are thousands of awareness and fundraising events taking place all over the country. From the entire office wearing pink and donating for the privilege, to all-pink bake sales, pink raffles, pink sponsored skydives and pink dares.</p>

<p>Though you may not be willing to dye your hair pink for Breast Cancer Awareness Month, Wear it Pink day is organised to raise money which is used for life-saving research into breast cancer - how to prevent it, detect it and cure it.</p>

<p>Knowing the signs of breast cancer could be the difference between living with the disease or dying from it. Here&rsquo;s our guide for understanding and recognising the potential signs of breast cancer so that more of us can achieve the former.</p>

<h2>Remember, not all sufferers of breast cancer are women.</h2>

<p>Though 55,000 women in the UK are diagnosed with breast cancer, around <a href="https://breastcancernow.org/information-support/have-i-got-breast-cancer/breast-cancer-in-men">370</a> men are also diagnosed each year, and many more go undiagnosed because they do not realise they are also at risk.</p>

<p>The signs of breast cancer can include:</p>

<ul>
	<li>A lump or swelling in the breast, upper chest or armpit. You might be able to feel the lump though not see it and you might only be able to feel the lump in certain positions - for example, only lying down or in a sitting position, but not standing.</li>
	<li>Changes to the size, shape or appearance of the breast. One breast suddenly bigger than the other, nipples suddenly out of alignment, a breast that once was bottom-heavy now fuller on top. The key is any change should be investigated.</li>
	<li>A change in skin texture can signify a potential breast cancer - this can mean puckering or dimpling of the skin.</li>
	<li>A change in the colour of the breast - the breast may look red or inflamed, or veins may be more prominent.</li>
	<li>A rash, crusting or changes to the nipple can signify problems. Sometimes skin conditions that can appear like eczema can end up being significant in a breast cancer diagnosis.</li>
	<li>Any unusual discharge from either nipple must be evaluated, no matter the consistency or colour, and this goes for both men and women.</li>
</ul>

<h2>Check your breasts regularly</h2>

<p>Checking your breasts for any changes is really as simple as looking, feeling and being familiar with your breasts. For women, this means understanding the normal changes your breasts may go through during your menstrual cycle and being aware of what is normal for you. Checking yourself regularly can help you to understand what is normal or not for your body, and help you detect any potential changes quickly and have the best chance of early treatment and recovery.</p>

<p>Breast Cancer Now has developed a handy app you can use to help you check your breasts, monitor any changes and set reminders for you to check at regular intervals. Download it <a href="https://breastcancernow.org/about-breast-cancer/what-are-the-signs-and-symptoms-of-breast-cancer/breast-check-now">here</a></p>

